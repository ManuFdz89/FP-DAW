public class OpeAsComp {

    public static void main(String[] args) {

        int inicial = 100;
        System.out.println("Valor inicial: "+inicial);

        inicial += 50;
        System.out.println("Despues de sumar 50 (+=): "+inicial);

        inicial -= 30;
        System.out.println("Despues de restar 30 (-=): "+inicial);

        inicial *= 2;
        System.out.println("Despues de multiplicar por 2 (*=): "+inicial);

        inicial /= 4;
        System.out.println("Despues de dividir entre 4 (/=): "+inicial);

    }
}
